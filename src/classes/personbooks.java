package classes;

import java.util.Date;

public class personbooks {
    private String name;
    private String name_amantdahande;
    private String writer;
    private String mohlat;
    private Date date;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName_getter() {
        return name_amantdahande;
    }

    public void setName_getter(String name_getter) {
        this.name_amantdahande = name_getter;
    }

    public String getWriter() {
        return writer;
    }

    public void setWriter(String writer) {
        this.writer = writer;
    }

    public String getMohlat() {
        return mohlat;
    }

    public void setMohlat(String mohlat) {
        this.mohlat = mohlat;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;

    }

}
